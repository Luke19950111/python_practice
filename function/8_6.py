def city_country(city_name, in_country):
    city_of_country = city_name + ', ' + in_country
    return  city_of_country

c_of_c_1 = city_country('Beijing', 'China')
print (c_of_c_1)

c_of_c_2 = city_country('New York', 'America')
print (c_of_c_2)

c_of_c_3 = city_country('Tokyo', 'Japan')
print(c_of_c_3)